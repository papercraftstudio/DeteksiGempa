package com.dytra.deteksigempa;

class StackSite {

    private String tanggal;
    private String jam;
    private String coordinates;
    private String lintang;
    private String bujur;
    private String magnitude;
    private String kedalaman;
    private String wilayah;

    String getTanggal() {
        return tanggal;
    }

    void setTanggal(String tanggal) {
        this.tanggal = tanggal;
    }

    String getJam() {
        return jam;
    }

    void setJam(String jam) {
        this.jam = jam;
    }

    String getCoordinates() {
        return coordinates;
    }

    void setCoordinates(String coordinates) {
        this.coordinates = coordinates;
    }

    String getLintang() {
        return lintang;
    }

    void setLintang(String lintang) {
        this.lintang = lintang;
    }

    String getBujur() {
        return bujur;
    }

    void setBujur(String bujur) {
        this.bujur = bujur;
    }

    String getMagnitude() {
        return magnitude;
    }

    void setMagnitude(String magnitude) {
        this.magnitude = magnitude;
    }

    String getKedalaman() {
        return kedalaman;
    }

    void setKedalaman(String kedalaman) {
        this.kedalaman = kedalaman;
    }

    String getWilayah() {
        return wilayah;
    }

    void setWilayah(String wilayah) {
        this.wilayah = wilayah;
    }

}